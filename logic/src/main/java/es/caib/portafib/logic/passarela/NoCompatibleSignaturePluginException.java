public class NoCompatibleSignaturePluginException extends Exception {

  public NoCompatibleSignaturePluginException() {
    super();
  }

  public NoCompatibleSignaturePluginException(String message, Throwable cause) {
    super(message, cause);
  }

  public NoCompatibleSignaturePluginException(String message) {
    super(message);
  }

  public NoCompatibleSignaturePluginException(Throwable cause) {
    super(cause);
  }

}
